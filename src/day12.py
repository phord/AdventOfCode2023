def matchsub(s, m):
    out = []
    width = 0
    for n in range(len(m)+1):
        if width + n - 1 > len(s):
            break
        mat = matchsubset(s, m, n)
        if mat:
            out.append((n, mat))
        if n < len(m):
            width += m[n]
    return out


def trymode(g, m):

    # print(g, m, count)

    # print(len(g))
    # print(l, len(g), s, sum(m))

    # run out of damage counters
    if not m:
        if '#' in ''.join([x[0] for x in g]):
            return 0
        else:
            return 1

    # run out of g, still have m
    if not g and m:
        # print("BAD ran out of g")
        return 0

    # Get possibilities for next matches
    poss = matchsub(g[0], m)

    total = 0
    for n,count in poss:
        total += count * trymode(g[1:], m[n:])
    return total

